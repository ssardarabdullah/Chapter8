def show_messages(messages):
    for message in messages:
        print(message)

# List of short text messages
text_messages = [
    "Hey, how are you?",
    "Don't forget the meeting at 10am.",
    "Lunch later?",
    "Happy birthday!",
    "Call me when you're free."
]

# Call the function with the list
show_messages(text_messages)